    private void checkDatabaseConnection() {
        try (Connection conn = DriverManager.getConnection(DB_URL)) {
            if (conn != null) {
                System.out.println("Connected to the database.");
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Failed to connect to the database: " + e.getMessage(),
                    "Database Connection Error", JOptionPane.ERROR_MESSAGE);
            System.exit(1); // Exit the application if the connection fails
        }
    }
    
    // Method to encrypt the message using the database
private String encryptMessage(String message) {
    StringBuilder encryptedMessage = new StringBuilder();
    try (Connection conn = DriverManager.getConnection(DB_URL, USER, PASSWORD);
         PreparedStatement pstmt = conn.prepareStatement("SELECT COL_ENC FROM T_ED WHERE `COL_CH` = ?")) {
        
        for (char c : message.toCharArray()) {
            pstmt.setString(1, String.valueOf(c)); // This line ensures case sensitivity
            try (ResultSet rs = pstmt.executeQuery()) {
                if (rs.next()) {
                    encryptedMessage.append(rs.getString("COL_ENC")); // Append encrypted character
                } else {
                    encryptedMessage.append(c); // Append original character if no match
                }
            }
        }
    } catch (SQLException e) {
        e.printStackTrace(); // Log the error to the console
        JOptionPane.showMessageDialog(this, "SQL Error: " + e.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
    }
    return encryptedMessage.toString();
}


// Method to decrypt the message using the database
private String decryptMessage(String encryptedMessage) {
    StringBuilder decryptedMessage = new StringBuilder();
    
    try (Connection conn = DriverManager.getConnection(DB_URL, USER, PASSWORD);
         PreparedStatement pstmt = conn.prepareStatement("SELECT `COL_CH` FROM T_ED WHERE `COL_ENC` = ?")) {
        
        for (int i = 0; i < encryptedMessage.length(); i++) {
            String currentChar = String.valueOf(encryptedMessage.charAt(i));
            pstmt.setString(1, currentChar); // This line ensures case sensitivity
            try (ResultSet rs = pstmt.executeQuery()) {
                if (rs.next()) {
                    decryptedMessage.append(rs.getString("COL_CH")); // Append decrypted character
                } else {
                    decryptedMessage.append(currentChar); // Append original character if no match
                }
            }
        }
    } catch (SQLException e) {
        e.printStackTrace(); // Log the error to the console
        JOptionPane.showMessageDialog(this, "SQL Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    
    System.out.println("Decrypted Message: " + decryptedMessage.toString()); // Debugging statement
    return decryptedMessage.toString();
}

